//logical AND  (&&):

let a = 100;
let b = 20;
let c = 50;
console.log(a > b && a < b);

// logical OR   (||):

console.log(a > b || a < b);

//logical NOT  (!):
console.log(!(a < b));

console.log(a > b && a > c && a < b);
console.log(a > b || a > c || a < b);
console.log((a > b && a < c) || a < b);

